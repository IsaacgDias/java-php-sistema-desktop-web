    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Funcionario f = new Funcionario();
            FuncionarioDao fdao = new FuncionarioDao();
            String email = String.valueOf(txtEmail.getText());
            String senha = String.valueOf(txtSenha.getText());
            f.setEmail(email);
            f.setSenha(senha);

            ResultSet rfuncionariodao = fdao.LoginFuncionario(email, senha);
            if (rfuncionariodao.next()) {
                if ("funcionario".equals(f.getTipo())) {
                    Area_Funcionario tela = new Area_Funcionario();
                    tela.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Email ou senha incorreto");
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton1ActionPerformed

